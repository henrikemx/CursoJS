console.log('\n----- LAÇO USANDO ESTRUTURA while -----\n')

var c = 1
while (c <= 5){
    console.log(`Passo ${c}`)
    c++
}

//-------------------------------

console.log('\n----- LAÇO USANDO ESTRUTURA do while -----\n')

var c = 1
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 5)

//-------------------------------

console.log('\n----- LAÇO USANDO ESTRUTURA for -----\n')

for (var c = 1;c <= 5;c++){
    console.log(`Passo ${c}`)
}
